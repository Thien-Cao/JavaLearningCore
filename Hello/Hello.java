
class Hello {
    public static void main(String[] args) {

        // Arthemetic Operator
        int number = 7;
        // ++number; -> pre increment -> increment and then fetch
        // number++; -> post increment -> fetch the value and then increment
        int result = number++;
        // Arthemetic Operator
        System.out.println(result);
        System.out.println(number);

    }
}